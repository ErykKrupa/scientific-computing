    tangentTest(x -> ((x - 2) ^ 2) + 1, x -> 2 * (x - 2), 0.0, 0.00001, 0.00001, 100)
    tangentTest(x -> ((x - 2) ^ 2) + 1, x -> 2 * (x - 2), 1.0, 0.00001, 0.00001, 100)
